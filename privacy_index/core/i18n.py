def normalize_lang(lang: str | None) -> str:
    if not lang:
        return "en"
    l = lang.strip().lower()
    if l in {"f", "fra", "fre", "français", "francais"}:
        return "fr"
    if l in {"e", "eng", "english"}:
        return "en"
    if l in {"g", "ger", "deu", "deutsch", "allemand"}:
        return "de"
    return l if l in SUPPORTED_LANGS else "en"


def ui(lang: str, key: str) -> str:
    lang = normalize_lang(lang)
    return UI.get(lang, UI["fr"]).get(key, UI["fr"].get(key, key))


